            <div class="article-content">
                <p>
                    高考前夕，北京律师张晓玲收到一封来自武汉的家长来信：《一个心碎母亲致所有高考考生公开信》。信中讲述了原本成绩优秀的儿子，因沉溺网游导致高考失利，至今未曾走出阴影的经历。并对高考考生“喊话”：在你们人生最宝贵的学习阶段，请一定别让自己的人生成为游戏！每个被毁掉的孩子，都浸泡着家长的无奈。张晓玲将这封信发到个人微博，再次引发舆论对青少年沉迷网游的关注。

                </p>
                <p>
                    十年寒窗苦读，就为高考“临门一脚”，虽然一场高考不会赢得所有，也不会输掉一生，但它总会给孩子们带来人生转折。个别孩子却因沉溺网游而导致高考失利。比如公开信中那位孩子自初三开始沉溺网游，后来发展到临近高考还有两个月时还逃课去玩网游，一度被学校勒令停课，从一位光彩无限的翩翩少年，变成目光呆滞、衣冠不整的沉迷者。面对此情此景，换了任何家长，都会痛陈网游之罪，并呼吁其他孩子“一定要远离网游、好好学习，决不能因为沉迷网游而葬送前途”。
                </p>
                <div align="center">
                    <img src="/Public/home/img/img_about_01.jpg" alt="">
                </div>
                <p>
                    那位写长文自认“斗不过网游”的心碎母亲，何尝不是许多家长的写照？网游之害并不仅仅是影响高考，而是对青少年整个健康成长过程的毒害。张晓玲律师还以自己的所见所闻印证了网游之害：“狗年春节，回乡过年，看到亲戚、同学的家庭因孩子陷入网游不能自拔而陷入无休止的家庭战争”，她因此介入到青少年“游戏防沉溺”的课题研究，自今年3月以来，她已收到百余位家长自述的案例，“因沉溺游戏，导致青少年成绩下滑者不说，凶杀、自残、精神分裂，甚至导致肌无力病症者更是触目惊心”。可见预防青少年过度沉溺网游，已到了刻不容缓的地步。
                </p>
                <div align="center">
                    <img src="/Public/home/img/img_about_01.jpg" alt="">
                </div>
                <p>
                    拿什么预防青少年过度沉溺网游？依靠家长与学校严防死守、围追堵截吗？孩子又非大人身上的附属物，可以走到哪带到哪。那位心碎母亲已证明此点。她的孩子从“魔兽世界”玩到“英雄联盟”再玩到“王者荣耀”，其间，“与其说我与孩子斗争了十几年，还不如说与网游斗争了十几年——但我输了，我们全家都输了，输得彻彻底底”。防沉迷要依靠孩子自制自律吗？在新鲜刺激、颇具诱惑力的事物面前，自制自律其实是个靠不住的东西，大人尚且如此，何况心智没成熟的孩子？正如那位心碎母亲的质疑：一辆汽车没有刹车系统行吗？一剂药物不标注用量行吗？
                </p>
                <p>
                    面对网游对青少年的猛攻，教育的力量真的微不足道。预防青少年过度沉溺网游，首先要靠以腾讯为代表的各个游戏厂商、平台在追求商业价值时，还要勇担社会责任。比如，不断开发内容健康向上、不容易让人沉迷、有助于青少年学习与成长的健康游戏。对于那些容易让孩子沉迷的游戏，要坚决做到防沉迷。比如腾讯，有专家说，以其强大的技术，为游戏设置一个限制未成年人玩耍时长的电脑插件是完全可以做到的。为何不去做呢？恐怕是无法控制其追逐利润最大化的欲望吧。
                </p>
                <p>
                    那位武汉的心碎母亲的公开信，不仅仅是针对当下的舆论热点高考生群体，而是对包括网游企业、监管部门在内的社会各界的悲愤呐喊。当网游企业在勇担社会责任上裹足不前时，监管部门就要严肃亮剑。一方面，要出台具有严厉处罚措施的文件、法律法规，让监管长出锋利牙齿，震慑威力才可能发挥，违规违法者才可能敬畏；另一方面，对网络游戏产品要加强审查，游戏产品入市前，应测评其对人们的精神控制程度，若达到危害人们身心健康的程度，必须禁止入市，或者责令开发者必须配套可检验的有效防沉迷措施。
                </p>
                <p>
                    网游之害猛于虎！这一社会之痛由来已久，社会应该给予高度、严重而持续的关注，而不仅仅因为高考来临才引发热议。
                </p>

